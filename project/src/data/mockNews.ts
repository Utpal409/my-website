export const getFeaturedNews = (): NewsArticle[] => {
  return mockNews.filter(news => news.isFeatured);
};

export const getLatestNews = (limit: number = 6): NewsArticle[] => {
  return [...mockNews]
    .sort((a, b) => new Date(b.publishedAt).getTime() - new Date(a.publishedAt).getTime())
    .slice(0, limit);
};

export const getNewsByCategory = (category: string): NewsArticle[] => {
  return mockNews.filter(news => news.category === category);
};

export const getNewsById = (id: string): NewsArticle | undefined => {
  return mockNews.find(news => news.id === id);
};